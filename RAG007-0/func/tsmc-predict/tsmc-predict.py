import numpy as np
from sklearn.linear_model import LinearRegression

# 1. 準備訓練數據
X = np.array([
    [2390, 2490, 2510],
    [2390, 2390, 2490],
    [2340, 2390, 2390],
    [2370, 2340, 2390],
    [2410, 2370, 2340],
    [2505, 2410, 2370],
    [2465, 2505, 2410],
    [2445, 2465, 2505],
    [2460, 2445, 2465],
    [2440, 2460, 2445],
    [2465, 2440, 2460],
    [2415, 2465, 2440],
    [2440, 2415, 2465],
    [2420, 2440, 2415],
    [2440, 2420, 2440],
    [2470, 2440, 2420],
    [2290, 2470, 2440]
])

y = np.array([
    2390, 2340, 2370, 2410, 2505, 2465, 2445, 2460,
    2440, 2465, 2415, 2440, 2420, 2440, 2470, 2290, 2320
])

# 2. 建立並訓練線性回歸模型
model = LinearRegression()
model.fit(X, y)

# 3. 取得參數
a1, a2, a3 = model.coef_  # 權重係數 (Coefficients)
b = model.intercept_      # 截距 (Intercept)
r2 = model.score(X, y)    # 決定係數 (R-squared)

# 4. 印出結果
print("--- 求解結果 ---")
print(f"a1 (前1天權重): {a1:.4f}")
print(f"a2 (前2天權重): {a2:.4f}")
print(f"a3 (前3天權重): {a3:.4f}")
print(f"b  (截距項)  : {b:.4f}")
print(f"R² 決定係數   : {r2:.4f}")

# 5. 印出擬合的線性方程式
print(f"\n線性回歸方程式：")
print(f"y = {a1:.4f} * x1 + {a2:.4f} * x2 + {a3:.4f} * x3 + {b:.4f}")